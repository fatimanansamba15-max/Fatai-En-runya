import streamlit as st
import torch
import io
import soundfile as sf
from transformers import AutoTokenizer, M2M100ForConditionalGeneration, pipeline

# 1. App Window Initialization
st.set_page_config(page_title="Runyankole Neural App", layout="centered")
st.title("🇺🇬 True Runyankole AI Translator")
st.markdown("---")


# 2. Optimized Pipeline / Engine Caching
@st.cache_resource
def load_speech_models():
    return pipeline("automatic-speech-recognition", model="openai/whisper-tiny")


@st.cache_resource
def load_translation_engine():
    # FIX: Revert to the globally hosted stable NLLB model base repository structure
    model_name = "facebook/nllb-200-distilled-600M"
    
    # Use standard NllbTokenizer and conditional generation classes to avoid OSError tracebacks
    tokenizer = AutoTokenizer.from_pretrained(model_name)
    model = M2M100ForConditionalGeneration.from_pretrained(model_name)
    
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = model.to(device)
    
    return tokenizer, model, device


# Instantiate Models
asr_pipeline = load_speech_models()
tokenizer, translation_model, device = load_translation_engine()

# FIX: Exact hardcoded vocabulary index keys specified by the Sunbird/SALT Translation Engine
language_tokens = {
    'eng': 256047, # Vector index token mapping for English
    'nyn': 256002  # Vector index token mapping for Runyankole
}

def translate_via_neural_net(text, direction_mode):
    try:
        if direction_mode == "English to Runyankole":
            src_token = language_tokens['eng']
            tgt_lang_code = "nyn_Latn" # Target string representation signature
        else:
            src_token = language_tokens['nyn']
            tgt_lang_code = "eng_Latn"

        # Tokenize the input phrase safely
        inputs = tokenizer(text, return_tensors="pt").to(device)

        # FIX: Directly overwrite the input prefix tensor index with the proper African language token mapping
        inputs['input_ids'][0][0] = src_token

        # Extract the target vocabulary token identifier 
        forced_bos_token_id = tokenizer.convert_tokens_to_ids(tgt_lang_code)

        # Generate translation matrices
        translated_tokens = translation_model.generate(
            **inputs,
            forced_bos_token_id=forced_bos_token_id,
            max_length=256,
            num_beams=4,      # Dynamic beam searching tracking for high context accuracy
            do_sample=False   
        )

        # Decode and safely pull index 0 to return a clean string
        result = tokenizer.batch_decode(translated_tokens, skip_special_tokens=True)
        if isinstance(result, list) and len(result) > 0:
            return result[0]
        return "No output generated by neural framework."
        
    except Exception as e:
        return f"Translation Subsystem Error Trace: {str(e)}"


# 3. Mode Configuration UI
direction = st.sidebar.selectbox("Flow Mode:", ["English to Runyankole", "Runyankole to English"])

# Initialize session state tracking spaces
if "voice_text" not in st.session_state:
    st.session_state.voice_text = "I love you so much"
if "last_processed_audio" not in st.session_state:
    st.session_state.last_processed_audio = None

# 4. Audio Processing Workflow Block
recorded_audio = st.audio_input("Record voice input")

# Only run transcription if the audio object is completely new
if recorded_audio is not None and recorded_audio != st.session_state.last_processed_audio:
    with st.spinner("Processing speech audio layer..."):
        try:
            audio_bytes = recorded_audio.read()
            audio_data, sample_rate = sf.read(io.BytesIO(audio_bytes))
            if len(audio_data.shape) > 1:
                audio_data = audio_data[:, 0]
            
            result = asr_pipeline({"raw": audio_data, "sampling_rate": sample_rate})
            
            st.session_state.voice_text = result["text"]
            st.session_state.last_processed_audio = recorded_audio
            st.rerun() 
        except Exception as e:
            st.error(f"Audio Decode Error: {e}")

if recorded_audio is None:
    st.session_state.last_processed_audio = None

# 5. Direct State Binding Layout
sentence = st.text_input("Input Phrase:", key="voice_text")
submit_button = st.button(label="Translate Text", type="primary")

# Execute conversion on button confirmation state matching
if submit_button and sentence:
    with st.spinner("Running Neural Net Inference..."):
        output_translation = translate_via_neural_net(sentence, direction)
        st.subheader("Neural Network Result:")
        st.success(output_translation)
